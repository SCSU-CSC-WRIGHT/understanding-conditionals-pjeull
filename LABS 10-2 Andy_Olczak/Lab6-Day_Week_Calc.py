day_of_week = int(input("Please enter the number corresponding to the day of the week: "))

if day_of_week == 1:
    print("It is Sunday.")
elif day_of_week == 2:
    print("It is Monday.")
elif day_of_week == 3:
    print("It is Tuesday.")
elif day_of_week == 4:
    print("It is Wednesday.")
elif day_of_week == 5:
    print("It is Thursday.")
elif day_of_week == 6:
    print("It is Friday.")
elif day_of_week == 7:
    print("It is Saturday.")
else:
    print("Invalid input. Try again.")